                <br><br><br>

                <h3>Low Fluid</H3>
                The easiest of brake problems simply involves a low amount of brake fluid in the master cylinder.
                Removing the cap and topping off the fluid is the way to fix it, but keep in mind, if the master
                cylinder is empty, pouring more fluid in will push air into the system. To fix this you will have to
                bleed the brakes. If you can't do this yourself, it will cost some labor hours with a
                mechanic.<br><br><br>

                <h3>Worn Pads or Drums</H3>
                A standard brake problem is worn pads and drums. Because brakes use friction to stop the car, as time
                wears on, so do the brake pads. It's just a simple fact of physics. Friction causes wear. After a while,
                brake pads must be replaced. You will detect a need to replace pads by hearing gradually increasing
                squeaking whenever you hit the brakes. If it goes on too long without being replaced, eventually you may
                have to replace the drums and/or rotors as well. At this point, what could have been a relatively
                inexpensive fix is now more expensive. A standard replacement of pads may cost LKR.20000.00 to LKR.30000.00, whereas a
                drum, rotor, and/or caliper replacement can get up into the several hundreds depending on the extent of
                the problem..<br><br><br>


                <h3>Master Cylinder and Wheel Cylinders</H3>
                The master cylinder is where the brake fluid is stored and through pressure, sent to each wheel
                cylinder. This fluid is what applies force to the pads and drums when you press down on the brake pedal.
                If the master cylinder goes bad, when you press down on the brake pedal, it might go to the floor and
                take longer to stop the car. At best, your braking power is greatly reduced; at worst, it could endanger
                your life by not stopping the car. The average cost of master cylinder repair is usually between LKR.50000.00
                and LKR.70000.00, but it can be more, again, depending on the scope of the fix. If individual wheel cylinders
                need to be replaced, the cost will go up.<br><br><br>

                The repair price for an entire brake job—rotors, calipers, drums, pads, cylinders—can run you LKR.250000.00 or
                more. Therefore, it's best to stay on top of brake repair and get the pads replaced when you hear
                squeaking. Components like master cylinders wear down after time, so eventually it will have to be
                replaced. As long as you keep brake fluid in it and keep an eye on it, though, you'll be safe to drive
                and can take the steps to have it fixed when it comes time. <br><br><br>



